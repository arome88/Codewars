//Make a simple function called greet that returns the most-famous "hello world!"

function greet() {
    const greeting = "hello world!"; //creates a constant variable called 'greeting' and assigns it the string value of "hello world!". The 'const' keyword is used to declare a variable that cannot be reassigned later.
    return greeting; // returns the value of the 'greeting' variable as the output of the 'greet' function. When the function is called, this value will be returned to the code that called the function
  }

  // the 'greet' function simply returns the string "hello world!" when called